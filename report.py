from fpdf import FPDF
from datetime import datetime
import os

os.makedirs("reports", exist_ok=True)


def clean_text(text):
    """
    Convert unsupported Unicode characters to ASCII-safe text
    for classic FPDF.
    """
    return (
        str(text)
        .replace("—", "-")
        .replace("–", "-")
        .replace("•", "*")
        .replace("✓", "OK")
        .replace("✔", "OK")
        .replace("✘", "X")
        .replace("→", "->")
        .replace("←", "<-")
        .replace("≥", ">=")
        .replace("≤", "<=")
        .replace("°", " deg ")
    )


class BloodClotReport(FPDF):

    def header(self):
        self.set_fill_color(180, 30, 30)
        self.rect(0, 0, 210, 18, "F")

        self.set_font("Helvetica", "B", 13)
        self.set_text_color(255, 255, 255)
        self.set_xy(0, 4)
        self.cell(0, 10, "Blood Clot Risk Assessment Report", align="C")

        self.set_text_color(0, 0, 0)
        self.ln(16)

    def footer(self):
        self.set_y(-15)

        self.set_font("Helvetica", "I", 8)
        self.set_text_color(150, 150, 150)

        self.cell(
            0,
            10,
            clean_text(
                "DISCLAIMER: This report is generated by an AI model for educational purposes only and does not constitute medical advice."
            ),
            align="C",
        )

    def section_title(self, title):
        self.set_font("Helvetica", "B", 11)
        self.set_fill_color(230, 230, 230)
        self.set_text_color(40, 40, 40)

        self.cell(
            0,
            9,
            clean_text(f"  {title}"),
            ln=True,
            fill=True,
        )
        self.ln(2)

    def info_row(self, label, value, highlight=False):

        self.set_font("Helvetica", "B", 10)
        self.set_text_color(80, 80, 80)
        self.cell(70, 8, clean_text(f"  {label}:"), border="B")

        self.set_font("Helvetica", "", 10)

        if highlight:
            self.set_text_color(180, 30, 30)
        else:
            self.set_text_color(30, 30, 30)

        self.cell(
            0,
            8,
            clean_text(value),
            border="B",
            ln=True,
        )

        self.set_text_color(0, 0, 0)


def generate_report(
    patient_data: dict,
    prediction: int,
    confidence: float,
    shap_plot_path: str = None,
) -> str:

    pdf = BloodClotReport()
    pdf.add_page()
    pdf.set_auto_page_break(auto=True, margin=20)

    # Metadata
    pdf.set_font("Helvetica", "", 9)
    pdf.set_text_color(120, 120, 120)

    report_id = datetime.now().strftime("%Y%m%d%H%M%S")

    pdf.cell(
        0,
        6,
        clean_text(
            f"Generated: {datetime.now().strftime('%B %d, %Y %H:%M')} | Report ID: BCR-{report_id}"
        ),
        ln=True,
    )

    pdf.ln(4)

    # Result Banner
    if prediction == 1:
        pdf.set_fill_color(255, 220, 220)
        banner_text = (
            f"RESULT: HIGH CLOT RISK ({confidence:.1f}% confidence)"
        )
    else:
        pdf.set_fill_color(210, 245, 225)
        banner_text = (
            f"RESULT: LOW CLOT RISK ({100 - confidence:.1f}% confidence)"
        )

    pdf.set_font("Helvetica", "B", 13)
    pdf.set_text_color(40, 40, 40)

    pdf.cell(
        0,
        14,
        clean_text(banner_text),
        align="C",
        fill=True,
        ln=True,
    )

    pdf.ln(6)

    # Patient Details
    pdf.section_title("Patient Clinical Details")

    field_map = {
        "age": ("Age", "years"),
        "sex": ("Sex", ""),
        "trestbps": ("Resting Blood Pressure", "mm Hg"),
        "chol": ("Cholesterol", "mg/dl"),
        "thalach": ("Max Heart Rate", "bpm"),
        "fbs": ("Fasting Blood Sugar >120", ""),
        "cp": ("Chest Pain Type", ""),
        "exang": ("Exercise Induced Angina", ""),
        "oldpeak": ("ST Depression", ""),
        "slope": ("ST Slope", ""),
        "ca": ("Major Vessels (0-3)", ""),
        "thal": ("Thalassemia", ""),
    }

    for key, (label, unit) in field_map.items():

        val = patient_data.get(key, "N/A")

        if unit:
            display = f"{val} {unit}"
        else:
            display = str(val)

        pdf.info_row(label, display)

    pdf.ln(6)

    # Risk Summary
    pdf.section_title("Risk Assessment Summary")

    risk_label = "HIGH RISK" if prediction == 1 else "LOW RISK"

    pdf.info_row(
        "AI Risk Classification",
        risk_label,
        highlight=(prediction == 1),
    )

    pdf.info_row(
        "Model Confidence",
        f"{confidence:.1f}%"
    )

    pdf.info_row(
        "Model Used",
        "XGBoost Classifier"
    )

    pdf.ln(4)

    pdf.set_font("Helvetica", "I", 9)
    pdf.set_text_color(80, 80, 80)

    if prediction == 1:
        pdf.multi_cell(
            0,
            6,
            clean_text(
                "Interpretation: The AI model has identified significant risk factors associated with blood clot formation. Immediate consultation with a qualified physician is strongly recommended."
            ),
        )
    else:
        pdf.multi_cell(
            0,
            6,
            clean_text(
                "Interpretation: The AI model found no significant combination of risk factors at this time. Maintain a healthy lifestyle and attend routine medical checkups."
            ),
        )

    pdf.ln(6)

    # SHAP Section
    if shap_plot_path and os.path.exists(shap_plot_path):

        pdf.section_title(
            "AI Explanation - Key Risk Factors (SHAP)"
        )

        pdf.set_font("Helvetica", "", 9)

        pdf.multi_cell(
            0,
            5,
            clean_text(
                "The chart below shows which patient features most influenced the AI prediction. Positive values increase risk while negative values reduce risk."
            ),
        )

        pdf.ln(3)

        try:
            pdf.image(shap_plot_path, x=15, w=175)
        except Exception as e:
            print("SHAP image skipped:", e)

        pdf.ln(4)

    # Recommendations
    pdf.section_title("Recommendations")

    pdf.set_font("Helvetica", "", 10)
    pdf.set_text_color(40, 40, 40)

    if prediction == 1:
        recommendations = [
            "1. Schedule an appointment with a physician.",
            "2. Consider D-Dimer testing if medically advised.",
            "3. Avoid prolonged inactivity.",
            "4. Stay hydrated.",
            "5. Review medications with your doctor.",
        ]
    else:
        recommendations = [
            "1. Maintain a balanced diet.",
            "2. Exercise regularly for at least 30 minutes daily.",
            "3. Monitor blood pressure and cholesterol.",
            "4. Avoid smoking.",
            "5. Schedule annual health checkups.",
        ]

    for rec in recommendations:
        pdf.cell(
            0,
            8,
            clean_text(rec),
            ln=True,
        )

    pdf.ln(4)

    # Save PDF
    timestamp = datetime.now().strftime("%Y%m%d_%H%M%S")

    filename = (
        f"reports/BloodClotReport_{timestamp}.pdf"
    )

    pdf.output(filename)

    print(f"Report saved: {filename}")

    return filename

